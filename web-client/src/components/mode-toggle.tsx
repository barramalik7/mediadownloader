import { Moon, Sun } from "lucide-react"
import { Button } from "@/components/ui/button"
import { useTheme } from "@/components/theme-provider"

export function ModeToggle() {
    const { theme, setTheme } = useTheme()

    const toggleTheme = () => {
        setTheme(theme === "dark" ? "light" : "dark")
    }

    // User Request:
    // "tombol night mode" (in light mode, show Moon)
    // "berubah menjadi tombol light mode" (in dark mode, show Sun)

    return (
        <Button variant="ghost" size="icon" onClick={toggleTheme}>
            {/* Sun Icon: Hidden in Light (scale-0), Visible in Dark (scale-100) */}
            <Sun className="h-[1.2rem] w-[1.2rem] rotate-90 scale-0 transition-all dark:rotate-0 dark:scale-100" />

            {/* Moon Icon: Visible in Light (scale-100), Hidden in Dark (scale-0) */}
            <Moon className="absolute h-[1.2rem] w-[1.2rem] rotate-0 scale-100 transition-all dark:-rotate-90 dark:scale-0" />

            <span className="sr-only">Toggle theme</span>
        </Button>
    )
}
